package com.atuldwivedi.learngroovy.lang.operator

/**
 * Relation operators
 * allow comparisons between objects
 */
assert 1 + 2 == 3 //equal
assert 3 != 4 //different

assert -2 < 3 //less than
assert 2 <= 2 //less than or equal
assert 3 <= 4 //less than or equal

assert 5 > 1 //greater than
assert 5 >= -2 //greater than or equal

